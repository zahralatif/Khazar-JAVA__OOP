public class DaysOfWeek {

  public static void main(String[] args) {
    String[] daysOfWeek = {
      "Monday",
      "Tuesday",
      "Wednesday",
      "Thursday",
      "Friday",
      "Saturday",
      "Sunday"
    };

    System.out.println("Days of the week: ");
    for (int i = 0; i < daysOfWeek.length; i++) {
      System.out.println(daysOfWeek[i]);
    }
  }
}
